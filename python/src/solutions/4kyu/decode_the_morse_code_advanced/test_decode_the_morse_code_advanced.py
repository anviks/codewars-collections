def test_example():
    assert (decode_morse(decode_bits(
        '1100110011001100000011000000111111001100111111001111110000000000000011001111110011111100111111000000110011001111110000001111110011001100000011'))
            == 'HEY JUDE')


def test_basic_bits_decoding():
    assert decode_morse(decode_bits("1")) == "E"
    assert decode_morse(decode_bits("101")) == "I"
    assert decode_morse(decode_bits("10001")) == "EE"
    assert decode_morse(decode_bits("10111")) == "A"
    assert decode_morse(decode_bits("1110111")) == "M"


def test_multiple_bits_per_dot():
    assert decode_morse(decode_bits("111")) == "E"
    assert decode_morse(decode_bits("1111111")) == "E"
    assert decode_morse(decode_bits("110011")) == "I"
    assert decode_morse(decode_bits("111110000011111")) == "I"
    assert decode_morse(decode_bits("11111100111111")) == "M"
    assert decode_morse(decode_bits("111000111000111")) == "S"


def test_extra_zeros():
    assert decode_morse(decode_bits("01110")) == "E"
    assert decode_morse(decode_bits("000000011100000")) == "E"


